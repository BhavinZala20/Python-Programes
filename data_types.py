# Operators in Python

''' 
1) Relational Operators {<, >, ==, !=}
2) Arithmetic Operators (+, -, *, **, /(flot), //(Division))
3) Logical Operators (|(OR), &(AND))
4) Boolean (True, False)
'''

a = 20;
b = 30;
print(a==b)

a = 3;
b = 2;
print(a**b)
print(a/b)
print(a//b)
print(a%b)

a = True
b = False
print(a|b)

a = -3+2*4-5
print(a)

a = -3+2%4-5
print(a)

a = 6+4/8-8**2
